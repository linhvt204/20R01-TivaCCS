int main(void)
{
    Init_SysCtl(80);
    Init_Led(RED | GREEN | BLUE);
    PinoutSet();
    // Already had Pull-up Resister on PF0
    while(GPIOPinRead(GPIO_PORTF_BASE, GPIO_PIN_0) != 0x00)
    {
    }
    // Enable Pull-up Resistor on PF4
    while(GPIOPinRead(GPIO_PORTF_BASE, GPIO_PIN_4) != 0x00)
    {
    }
    while (1)
    {
        LEDS ^= BLUE;
        delay_s(1);
        LEDS ^= GREEN;
        delay_s(1);
        LEDS ^= RED;
        delay_s(1);

    }
}

